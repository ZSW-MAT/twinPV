        /// <param name="n">Complex refractive index</param>
        public static Complex<double> GetEfieldAmplitudeFromIntensity(double intensity, Complex<double> n)
        {
            return Complex<double>.Sqrt(2 * intensity / (n * physConstants.c * physConstants.eps0));
        }

        /// <summary>
        /// returns the Inverse of a 2x2 complex Matrix (only works for 2x2!!!)
        /// </summary>
        /// <param name="matrix">copmlex input matrix, where the inverse will be given back</param>
        public static Matrix<Complex<double>> GetInverse2x2(Matrix<Complex<double>> matrix)
        {
            var inverseDet = 1 / (matrix[0, 0] * matrix[1, 1] - matrix[0, 1] * matrix[1, 0]);

            Matrix<Complex<double>> inverse = Matrix.Create<Complex<double>>(2, 2);
            inverse[0, 0] = inverseDet * matrix[1, 1];
            inverse[0, 1] = -inverseDet * matrix[0, 1];
            inverse[1, 0] = -inverseDet * matrix[1, 0];
            inverse[1, 1] = inverseDet * matrix[0, 0];

            return inverse;
        }

        /// <summary>
        /// simple method for transfer matrix calculations from http://people.ece.umn.edu/users/taylo589/tmm.shtml
        /// </summary>
        static void calculateTMMComplexMatrix()
        {
            //http://people.ece.umn.edu/users/taylo589/tmm.shtml

            var lam = 666e-9;
            var a0 = new Complex<double>(7 * Math.PI / 180, 0);

            var nd = new List<(Complex<double> n, double d)>();

            nd.Add((new Complex<double>(1, 0), 0));
            nd.Add((new Complex<double>(5, 0.1), 100e-9));
            nd.Add((new Complex<double>(1, 0), 0));

            var n0 = nd.First().n;
            var nS = nd.Last().n;
            var sin0 = Complex<double>.Sin(a0);
            var aS = Complex<double>.Asin(n0 / nS * sin0);

            // differentiate between s-polarization and p-polarization
            var D0_s = Matrix.Create(new Complex<double>[,] { { new Complex<double>(1, 0), new Complex<double>(1, 0) }, { Complex<double>.Cos(a0) * n0, -Complex<double>.Cos(a0) * n0 } });
            var D0_p = Matrix.Create(new Complex<double>[,] { { Complex<double>.Cos(a0), Complex<double>.Cos(a0) }, { n0, -n0 } });
            var DS_s = Matrix.Create(new Complex<double>[,] { { new Complex<double>(1, 0), new Complex<double>(1, 0) }, { Complex<double>.Cos(aS) * nS, -Complex<double>.Cos(aS) * nS } });
            var DS_p = Matrix.Create(new Complex<double>[,] { { Complex<double>.Cos(aS), Complex<double>.Cos(aS) }, { nS, -nS } });

            // initial matrix (superstrate invD)
            var M_s = D0_s.GetInverse(); // initial matrix
            var M_p = D0_p.GetInverse(); // initial matrix

            Console.WriteLine(M_s);

            // inner layers
            for (var i = 1; i < (nd.Count - 1); i++)
            {
                Complex<double> n = nd[i].n;
                double d = nd[i].d;
                Complex<double> a = Complex<double>.Asin(n0 / n * sin0);

                var D_s = Matrix.Create(new Complex<double>[,] { { new Complex<double>(1, 0), new Complex<double>(1, 0) }, { Complex<double>.Cos(a) * n, -Complex<double>.Cos(a) * n } });
                var D_p = Matrix.Create(new Complex<double>[,] { { Complex<double>.Cos(a), Complex<double>.Cos(a) }, { n, -n } });
                Complex<double> ph = 2 * Math.PI * d / lam * Complex<double>.Cos(a) * n;
                var P = Matrix.Create(new Complex<double>[,] { { Complex<double>.Exp(-Complex<double>.I * ph), new Complex<double>(0, 0) },
                    { new Complex<double>(0, 0), Complex<double>.Exp(Complex<double>.I * ph) } });
                M_s *= D_s * P * D_s.GetInverse();
                M_p *= D_p * P * D_p.GetInverse();
